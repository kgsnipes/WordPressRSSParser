package sample;

import java.util.Date;
import java.util.List;

public class WordPressRSSItem {
	
	private String title;
	private String link;
	private String commentLink;
	private Date publishDate;
	private String creator;
	private List<String> categories;
	private String guid;
	private String description;
	private String content;
	private String commentRssLink;
	private Integer comments;
	private List<String> mediaImages;
	
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getLink() {
		return link;
	}
	public void setLink(String link) {
		this.link = link;
	}
	public String getCommentLink() {
		return commentLink;
	}
	public void setCommentLink(String commentLink) {
		this.commentLink = commentLink;
	}
	public Date getPublishDate() {
		return publishDate;
	}
	public void setPublishDate(Date publishDate) {
		this.publishDate = publishDate;
	}
	public String getCreator() {
		return creator;
	}
	public void setCreator(String creator) {
		this.creator = creator;
	}
	public List<String> getCategories() {
		return categories;
	}
	public void setCategories(List<String> categories) {
		this.categories = categories;
	}
	public String getGuid() {
		return guid;
	}
	public void setGuid(String guid) {
		this.guid = guid;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	public String getCommentRssLink() {
		return commentRssLink;
	}
	public void setCommentRssLink(String commentRssLink) {
		this.commentRssLink = commentRssLink;
	}
	public Integer getComments() {
		return comments;
	}
	public void setComments(Integer comments) {
		this.comments = comments;
	}
	public List<String> getMediaImages() {
		return mediaImages;
	}
	public void setMediaImages(List<String> mediaImages) {
		this.mediaImages = mediaImages;
	}
	
	
	

}
